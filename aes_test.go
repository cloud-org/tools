package tools

import "testing"

func TestAESCrypto_Encrypt(t *testing.T) {
	a := &AESCrypto{
		Key: []byte("123F99CtUajPF123"),
	}
	source := "hello world"
	res, err := a.Encrypt([]byte(source))
	if err != nil {
		t.Errorf("err is %v\n", err)
		return
	}
	t.Log(res)

	value, err := a.Decrypt(res)
	if err != nil {
		t.Errorf("err is %v\n", err)
		return
	}

	t.Log("source is", value)
}
